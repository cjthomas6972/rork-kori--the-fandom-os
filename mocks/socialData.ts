export function getPostsByUniverse(universeId: string): Post[] {
  return POSTS.filter(p => p.universeId === universeId && p.scope === 'universe')
    .sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime());
}

export function getPostsByGuild(guildId: string): Post[] {
  return POSTS.filter(p => p.guildId === guildId)
    .sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime());
}

export function getEventsByUniverse(universeId: string): EventMeetup[] {
  const now = new Date();
  return EVENTS.filter(e => e.universeId === universeId && e.startTime > now)
    .sort((a, b) => a.startTime.getTime() - b.startTime.getTime());
}

export function getEventsByGuild(guildId: string): EventMeetup[] {
  return EVENTS.filter(e => e.guildId === guildId)
    .sort((a, b) => a.startTime.getTime() - b.startTime.getTime());
}

export function getUserGuilds(userId: string): GuildFull[] {
  return GUILDS_FULL.filter(g => g.memberIds.includes(userId));
}
