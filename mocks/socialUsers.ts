export function getSocialUserById(userId: string): SocialUser | undefined {
  return SOCIAL_USERS.find(user => user.id === userId);
}

export function getSocialUsersByUniverse(universeId: string): SocialUser[] {
  return SOCIAL_USERS.filter(user => user.activeUniverseIds.includes(universeId));
}

export function getUserUniverseProfile(userId: string, universeId: string): UserUniverseProfile | undefined {
  const user = getSocialUserById(userId);
  return user?.universeProfiles.find(p => p.universeId === universeId);
}
